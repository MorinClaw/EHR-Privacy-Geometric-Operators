#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Plot results generated by the privacy evaluation protocol.

This script visualizes:
  - Attack A: reconstruction R2 by leakage level
  - Attack B: Re-id@1 by operator / candidate size
  - (Attacks C/D can be added similarly)
"""
from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd

SCRIPT_DIR = Path(__file__).resolve().parent
OUT_DEFAULT = SCRIPT_DIR.parent / "results"


def plot_attack_a(out_dir: Path) -> None:
    """Attack A: plot R2 grouped by leakage and operator (linear model only)."""
    path = out_dir / "tables" / "table_attack_a_reconstruction.csv"
    if not path.exists():
        print(f"Skip plot for Attack A: missing {path}")
        return
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        print("Skip plotting: matplotlib is not installed")
        return

    df = pd.read_csv(path)
    df = df[df["model"] == "linear"].copy()
    if df.empty:
        return
    figs_dir = out_dir / "figs"
    figs_dir.mkdir(parents=True, exist_ok=True)

    for setting in ["z", "phys"]:
        sub = df[df["setting"] == setting]
        if sub.empty:
            continue
        # Aggregate by var, operator, and leakage (mean R2).
        agg = sub.groupby(["var", "operator", "leakage"], as_index=False)["R2"].mean()
        piv = agg.pivot_table(index=["var", "operator"], columns="leakage", values="R2")
        if piv.empty:
            continue
        ax = piv.plot(kind="bar", figsize=(max(8, len(piv) * 0.3), 4), width=0.8)
        ax.set_ylabel(r"$R^2$ (reconstruction, linear)")
        ax.set_title(f"Attack A: Reconstruction R² by leakage ({setting})")
        ax.legend(title="Leakage")
        plt.xticks(rotation=45, ha="right")
        plt.tight_layout()
        plt.savefig(figs_dir / f"plot_attack_a_R2_{setting}.pdf")
        plt.close()
        print(f"  plot_attack_a_R2_{setting}.pdf")


def plot_attack_b(out_dir: Path) -> None:
    """Attack B: plot Re-id@1 grouped by operator and candidate size m."""
    path = out_dir / "tables" / "table_attack_b_linkage.csv"
    if not path.exists():
        return
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        return
    df = pd.read_csv(path)
    if df.empty:
        return
    figs_dir = out_dir / "figs"
    figs_dir.mkdir(parents=True, exist_ok=True)
    agg = df.groupby(["operator", "m"], as_index=False)["Reid_at_1"].mean()
    for setting in ["z", "phys"]:
        sub = df[df["setting"] == setting]
        if sub.empty:
            continue
        piv = sub.pivot_table(index="operator", columns="m", values="Reid_at_1", aggfunc="mean")
        ax = piv.plot(kind="bar", figsize=(6, 4))
        ax.set_ylabel("Re-id@1")
        ax.set_title(f"Attack B: Re-identification @1 ({setting})")
        ax.legend(title="Candidate size m")
        plt.xticks(rotation=25)
        plt.tight_layout()
        plt.savefig(figs_dir / f"plot_attack_b_Reid_{setting}.pdf")
        plt.close()
        print(f"  plot_attack_b_Reid_{setting}.pdf")


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--out-dir", type=Path, default=OUT_DEFAULT)
    args = p.parse_args()
    out_dir = Path(args.out_dir)
    plot_attack_a(out_dir)
    plot_attack_b(out_dir)
    print("Plotting completed:", out_dir / "figs")


if __name__ == "__main__":
    main()
